    private void op_resActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_op_resActionPerformed
        if( !op && !etiqueta1.getText().equals("") ){
            etiqueta_op.setText( "-" );
            recorrido = 2;
            numero1="";
            contador_negativo = 0;
            op = true;
        }else if( recorrido == 1 && contador_negativo == 0 ){
            if( etiqueta1.getText().equals("") ){
                numero1 += "-";
                etiqueta1.setText( numero1 );
                contador_negativo = 1;
            }
        } else if ( recorrido == 2 && contador_negativo == 0 ){
            if( etiqueta2.getText().equals("") ){
                numero1 += "-";
                etiqueta2.setText( numero1 );
                contador_negativo = 1;
            }
        }
        contador_punto = 0;
        
    }//GEN-LAST:event_op_resActionPerformed

